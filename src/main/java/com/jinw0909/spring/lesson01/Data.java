package com.jinw0909.spring.lesson01;

public class Data {
	// id, 이름
	private int id;
	private String name;
	
	// getter setter 추가
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	
}
